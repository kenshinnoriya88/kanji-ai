/**
 * 駅名を正規化して座標を検索する
 * 「渋谷」「渋谷駅」どちらでも対応
 */
export function findStationCoords(name: string): [number, number] | null {
  const withSuffix = name.endsWith("駅") ? name : name + "駅";
  const withoutSuffix = name.endsWith("駅") ? name.slice(0, -1) : name;
  return STATION_COORDS[withSuffix] ?? STATION_COORDS[withoutSuffix] ?? null;
}
