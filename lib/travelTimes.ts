/** 駅名を正規化（「駅」の有無を吸収） */
function normalize(name: string): string {
  return name.endsWith("駅") ? name : name + "駅";
}

/** 2点間のHaversine距離(km) */
function haversine(lat1: number, lon1: number, lat2: number, lon2: number): number {
  const R = 6371;
  const dLat = ((lat2 - lat1) * Math.PI) / 180;
  const dLon = ((lon2 - lon1) * Math.PI) / 180;
  const a =
    Math.sin(dLat / 2) ** 2 +
    Math.cos((lat1 * Math.PI) / 180) *
      Math.cos((lat2 * Math.PI) / 180) *
      Math.sin(dLon / 2) ** 2;
  return R * 2 * Math.atan2(Math.sqrt(a), Math.sqrt(1 - a));
}

/**
 * 駅名から各エリアへの所要時間(分)を返す。
 * DBにない駅は座標から最近傍ハブ駅を探して加算推定。
 * coords が提供された場合に最近傍検索を行う。
 */
export function getTravelTimes(
  stationName: string,
  fallbackCoords?: [number, number]
): Record<AreaName, number> | null {
  const key = normalize(stationName);
  if (STATION_TRAVEL_TIMES[key]) {
    return STATION_TRAVEL_TIMES[key].times;
  }

  if (!fallbackCoords) return null;

  // 最近傍ハブ駅を探す
  let nearestKey = "";
  let nearestDist = Infinity;
  for (const [k, data] of Object.entries(STATION_TRAVEL_TIMES)) {
    const d = haversine(fallbackCoords[0], fallbackCoords[1], data.coords[0], data.coords[1]);
    if (d < nearestDist) {
      nearestDist = d;
      nearestKey = k;
    }
  }

  if (!nearestKey) return null;

  // 最近傍駅の所要時間に、その駅までの推定移動時間を加算
  // 平均的な都市近郊の電車速度 35km/h で概算
  const extraMin = Math.round((nearestDist / 35) * 60);
  const base = STATION_TRAVEL_TIMES[nearestKey].times;
  return {
    新宿: base["新宿"] + extraMin,
    渋谷: base["渋谷"] + extraMin,
    池袋: base["池袋"] + extraMin,
    東京: base["東京"] + extraMin,
    品川: base["品川"] + extraMin,
    上野: base["上野"] + extraMin,
    恵比寿: base["恵比寿"] + extraMin,
  };
}
